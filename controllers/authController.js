export const registerController = async (req, res) => {
  try {
    const { name, email, pass1, pass2, phone, address, answer, role } = req.body;
    //validations
    if (!name) {
      return res.send({ error: "Name is Required" });
    }
    if (!email) {
      return res.send({ error: "Email is Required" });
    }
    if (!pass1) {
      return res.send({ error: "Password is Required" });
    }
    if (!pass2) {
      return res.send({ error: "Confirm your Password" });
    }
    if (!phone) {
      return res.send({ error: "Phone no is Required" });
    }
    if (!address) {
      return res.send({ error: "Address is Required" });
    }
    if (!answer) {
      return res.send({ error: "Answer is required" });
    }
    if (!role) {
      return res.send({ error: "Role is required" });
    }

    if(pass1 != pass2){
      return res.send({ error: "Password Mismatch: Please enter the correct password to confirm."});
    }
    //check user
    const exisitingUser = await userModel.findOne({ email });
    //exisiting user
    if (exisitingUser) {
      return res.status(200).send({
        success: false,
        message: "Already Registered please login",
      });
    }
    //register user
    const hashedPassword = await hashPassword(pass1);
    //save
    const user = await new userModel({
      name,
      email,
      phone,
      address,
      password: hashedPassword,
      answer,
      role
    }).save();

    res.status(201).send({
      success: true,
      message: "User Register Successfully",
      user,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: "Error in Registeration",
      error,
    });
  }
};

